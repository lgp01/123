package com.lgp.domain;

public class Per {

	private int id;
	
	private String name;
	
	private char sex;
	
	private int tel;
	
	private String email;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public char getSex() {
		return sex;
	}

	public void setSex(char sex) {
		this.sex = sex;
	}

	public int getTel() {
		return tel;
	}

	public void setTel(int tel) {
		this.tel = tel;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	@Override
	public String toString() {
		return "Per [id=" + id + ", name=" + name + ", sex=" + sex + ", tel=" + tel + ", email=" + email + "]";
	}
	
	
}
